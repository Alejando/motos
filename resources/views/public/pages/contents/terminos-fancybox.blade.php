<h3>MECANICA DE PARTICIPACION EN LAS SUBASTAS</h3>
<p>
    <b>Derecho de Asiento dentro de una Subasta.</b>
</p>
<p>
    El Usuario, previamente registrado y autenticado dentro de nuestro sistema con su nombre de usuario y contraseña, deberá pagar una cuota, llamada “Asiento”, para tener derecho a ingresar a una sala virtual donde se llevará a cabo la subasta. Dicho pago podrá hacerse desde el momento de la publicación de la subasta hasta 60 minutos antes de iniciada.
</p>
<p>
    El precio del Asiento se definirá en la publicación de cada artículo.
</p>
<p>
    <b>Tiempo de duración de la Subasta</b>
</p>
<p>
    El tiempo de duración de la subasta estará definido en cada artículo a subastar con una Fecha y Hora de inicio, y una Fecha y Hora de Fin de la Subasta.
</p>
<p>
    <b>Que hacer durante la Subasta</b>
</p>
<p>
    El Usuario tendrá disponible un panel de acción “Sala Virtual” en el cual podrá seguir el desarrollo de la subasta, ver el tiempo restante, ver la última oferta, ver la cantidad de ofertas disponibles, ver quién va a la delantera en la subasta y hacer ofertas.
</p>
<p>
    <b>Cantidad de Ofertas por Usuario</b>
</p>
<p>
    Cada Usuario dispone de 18 oportunidades de oferta durante la subasta, que tendrá que administrar según su estrategia, para al final ser el mejor ofertante y llevarse el producto.
</p>
<p>
    <b>Quien Se lleva la Subasta</b>
</p>
<p>
    El Usuario que al finalizar el tiempo de la subasta tiene la Oferta más alta, es el acreedor al artículo por la cantidad ofertada, y que además haya cumplido con el mínimo de 12 ofertas. Si al finalizar el tiempo de la subasta, el Usuario con la oferta más alta, no cumplió con haber hecho mínimo 12 ofertas, el siguiente Usuario con la oferta más alta y que si cumpla con esta característica será el que se lleve la subasta.
</p>
<p>
    <b>Quien NO se lleva la subasta</b>
</p>
<p>
    Los Usuarios que al terminar la subasta no tuvieron la oferta más alta. Aquél Usuario que al terminar la subasta tiene la oferta más alta, pero no ha cumplido con el mínimo de 12 ofertas durante el transcurso de la Subasta.
</p>
<p>
    Los Usuarios que no se llevaron la subasta no están obligados a pagar lo que hayan ofertado durante el transcurso de la subasta.
</p>
<p>
    <b>Tiempo de Inactividad</b>
</p>
<p>
    Es el tiempo en el que el Usuario permanece sin ofertar dentro de una subasta. Existe un límite de 2 minutos de inactividad. El Usuario será penalizado con una oportunidad menos para hacer Oferta por cada 2 minutos de inactividad.
</p>
<p>
    <b>Retardos</b>
</p>
<p>
    El Usuario que tenga su Asiento pagado, y llegue tarde a la Sala, será penalizado con una oportunidad menos de oferta por cada 2 minutos de retardo. Debiendo de tomar en cuenta que se requieren de mínimo 12 ofertas para poder llevarse la subasta.
</p>
